#include <stdio.h>

int main()
{
    int a = 0;
    int b = 1;

    printf("a = %d\n", a);
    printf("b = %d\n", b);
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a %% b = %d\n", a % b);
    printf("a++ = %d\n", a++);
    printf("++a = %d\n", ++a);
    printf("a-- = %d\n", a--);
    printf("--a = %d\n", --a);

    return 0;
}
